#creacion de la clase
class animal :
    pass
class perro(animal):
    def nombre (self):
        print("boby")
    def tipo_animal(self):
        print("carnivoro")
class gato(animal):
    def nombre(self):
        print("lolo")
    def tipo_animal(self):
        print("carnivoro")
animal1 = perro()
animal2 = gato()
print(animal1.nombre())
print(animal1.tipo_animal())
print(animal2.nombre())
print(animal2.tipo_animal())
